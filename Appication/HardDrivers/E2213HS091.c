/**
 * @brief	画点
 * @param	xPos：X轴坐标
 * @param	yPos：Y轴坐标
 * @param	color：颜色（只能红黑白）
 * @retval	none
 */
void E2213HS091_DrawPoint(uint8_t xPos, uint8_t yPos, enum ENUM_COLOR color)
{
    uint16_t i;
    uint8_t n;
    uint8_t temp;
    if (1) {
        /* Rotate 180' */
        // xPos = E2213HS091_XPOS_MAX - xPos;
        // yPos = E2213HS091_YPOS_MAX - yPos;
        /* Rotate 270' */
        temp = xPos;
        xPos = yPos;
        yPos = E2213HS091_YPOS_MAX - temp;
    }

    /* 判断坐标是否合法 */
    if ((xPos > E2213HS091_XPOS_MAX) || (yPos > E2213HS091_YPOS_MAX))
    {
        return;
    }
    /* 计算要对数组的序号为i的第n位进行操作 */
    i = yPos * E2213HS091_BUFFER_WIDTH_SIZE + (xPos / 8);
    n = 7- (xPos % 8);
    /* 进行位操作 */
    switch(color)
    {
        case WHITE:
            ClearBit(E2213HS091_FirstFrameBuffer[i], n);
            // ClearBit(E2213HS091_SecondFrameBuffer[i], n);
            break;
        case BLACK:
            SetBit(E2213HS091_FirstFrameBuffer[i], n);
            // ClearBit(E2213HS091_SecondFrameBuffer[i], n);
            break;
        default: 
            return;
    }
}

