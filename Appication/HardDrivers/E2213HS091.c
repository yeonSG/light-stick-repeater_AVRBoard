/**
 * @brief	等待，直到BUSY脚为高电平
 * @param	none
 * @retval	none
 */
static void E2213HS091_WaiteUntilNotBusy(void)
{
    while (E2213HS091_BUSY_READ() != HIGH)  // LOW 이면 바쁨
    { 
        HAL_Delay(10);
    }    
}

